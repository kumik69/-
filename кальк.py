x1 = int(input("Введите первое число "))
x2 = int(input("Введите второе число "))
e1 = input("Введите оператор ")
if e1 == "+":
    print(x1 + x2)
elif e1 == "-":
    print(x1-x2)
elif e1 == "*":
    print(x1*x2)
elif e1 == "/":
    print(x1/x2)
elif e1 == "//":
    print(x1//x2)
elif e1 == "**":
    print(x1**x2)
elif e1 == "%":
    print(x1%x2)